package cgc.models.response;

/**
 * The data that makes up an address match
 */
public class AddressComponents {
    /**
     * The zip code
     */
    public String zip;
    /**
     * The street name
     */
    public String streetName;
    /**
     * The prefix type
     */
    public String preType;
    /**
     * The city name
     */
    public String city;
    /**
     * The prefix direction
     */
    public String preDirection;
    /**
     * The suffix direction
     */
    public String suffixDirection;
    /**
     * The starting address
     */
    public String fromAddress;
    /**
     * The state name
     */
    public String state;
    /**
     * The suffix type
     */
    public String suffixType;
    /**
     * The ending address
     */
    public String toAddress;
    /**
     * The suffix qualifier
     */
    public String suffixQualifier;
    /**
     * The prefix qualifier
     */
    public String preQualifier;
}
